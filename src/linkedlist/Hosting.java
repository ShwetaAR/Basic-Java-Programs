package linkedlist;

public class Hosting {

	Integer i;
	String string;

	Integer j;

	public Hosting(int i, String string, int j) {
		super();
		this.i = i;
		this.string = string;
		this.j = j;
	}

	public Integer getI() {
		return i;
	}

	public void setI(Integer i) {
		this.i = i;
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}

}
